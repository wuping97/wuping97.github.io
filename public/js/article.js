$('.artical-content').mouseenter(function () {
		// console.dir('鼠标进入事件');
		$('.artical-title').css('margin-top','20%');
		$('.artical-excerpt').css('display', 'block');
	});

$('.artical-content').mouseleave(function () {
	$('.artical-title').css('margin-top','28%');
	$('.artical-excerpt').css('display', 'none');
});

